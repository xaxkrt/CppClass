/*
# 04-02-01-求两门课程成绩总分(2)

## 题目描述

已知小A和小B两名同学。在期末考试中，小A语文考了96分，数学考了92分，求他的总分并输出。小B语文考了95分，数学考了90分，求他的总分并输出。

## 输入格式

本题无输入。

## 输出格式

输出占2行，第1行为小A的总分。第2行为小B的总分。

## 提示

### 本题出处
本题源自以下教材的编程习题：王桂平, 雷雪林编著. 小学生C++编程启蒙.
*/

#include <iostream>
using std::cout;
using std::endl;

int main() {
    // 小A
    cout << 96 + 92 << endl;

    // 小B
    cout << 95 + 90 << endl;

    return 0;
}
